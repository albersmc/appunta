package com.sergiandreplace.appunta.orientation;

public class Orientation {

	private float x = 0;
	private float z = 0;
	private float y = 0;

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getZ() {
		return z;
	}

	public void setZ(float z) {
		this.z =  z;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y =  y;
	}

}
